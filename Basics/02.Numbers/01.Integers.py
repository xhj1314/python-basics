# 整数

# 什么是整数？
# 整数是没有小数部分的数字，可以是正数、负数或零

# 创建整数变量
a = 10      # 正整数
b = -3      # 负整数
c = 0       # 零

# 打印整数
print("整数示例:", a, b, c)

# 整数可以进行算术运算
print("\n算术运算:")
print("a + b =", a + b)   # 加法: 10 + (-3) = 7
print("a - b =", a - b)   # 减法: 10 - (-3) = 13
print("a * b =", a * b)   # 乘法: 10 * (-3) = -30
print("a // b =", a // b) # 整除: 10 // (-3) = -4
print("a % b =", a % b)   # 取模: 10 % (-3) = -2

# 整数可以是任意大小
big_number = 12345678901234567890
print("\n大整数:", big_number)
