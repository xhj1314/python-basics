# 算术运算

# Python支持各种算术运算

# 1. 加法
print("加法:")
print("5 + 3 =", 5 + 3)
print("5 + 2.5 =", 5 + 2.5)

# 2. 减法
print("\n减法:")
print("10 - 4 =", 10 - 4)
print("10 - 2.5 =", 10 - 2.5)

# 3. 乘法
print("\n乘法:")
print("6 * 7 =", 6 * 7)
print("6 * 2.5 =", 6 * 2.5)

# 4. 除法（总是返回浮点数）
print("\n除法:")
print("10 / 3 =", 10 / 3)

# 5. 整除（向下取整）
print("\n整除:")
print("10 // 3 =", 10 // 3)

# 6. 取模（求余数）
print("\n取模:")
print("10 % 3 =", 10 % 3)

# 7. 幂运算
print("\n幂运算:")
print("2 ** 3 =", 2 ** 3)      # 2的3次方
print("2 ** 0.5 =", 2 ** 0.5)  # 2的平方根
