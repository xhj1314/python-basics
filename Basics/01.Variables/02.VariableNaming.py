# 变量命名规则

# 变量名必须遵守以下规则：

# 规则1：只能使用字母、数字、下划线
user_name = "张三"    # 正确：使用下划线
age2 = 20            # 正确：字母和数字
_private = "秘密"     # 正确：下划线开头

# 规则2：不能以数字开头
# 2name = "李四"      # 错误！不能以数字开头

# 规则3：区分大小写
name = "小王"
Name = "大王"
NAME = "老王"
print(name, Name, NAME)  # 输出三个不同的值

# 规则4：不能使用Python关键字
# if = 10      # 错误！if是关键字
# for = 20     # 错误！for是关键字
# while = 30   # 错误！while是关键字

# 好的命名习惯：
# 1. 使用有意义的名字
student_name = "张三"    # 好的命名
x = "张三"              # 不好的命名（不知道x是什么）

# 2. 使用下划线分隔多个单词（蛇形命名法）
user_age = 18          # 好的命名
userAge = 18           # 也可以，但不推荐

# 3. 避免使用中文（虽然Python支持）
# 姓名 = "张三"         # 不推荐
name = "张三"          # 推荐
