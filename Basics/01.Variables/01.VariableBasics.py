# 变量基础

# 什么是变量？
# 变量就像一个盒子，用来存储数据
# 我们可以给这个盒子起个名字，方便以后使用

# 创建变量（定义变量）
name = "小明"      # 创建一个叫name的变量，存储"小明"
age = 18          # 创建一个叫age的变量，存储18
height = 1.75     # 创建一个叫height的变量，存储1.75
is_student = True # 创建一个叫is_student的变量，存储True

# 使用变量
print("姓名:", name)      # 输出: 姓名: 小明
print("年龄:", age)       # 输出: 年龄: 18
print("身高:", height)    # 输出: 身高: 1.75
print("是学生:", is_student)  # 输出: 是学生: True

# 变量可以存储不同类型的数据
# 字符串：用引号包围的文字
# 整数：没有小数的数字
# 浮点数：有小数的数字
# 布尔值：True或False
