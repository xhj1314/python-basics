# 元组基础

# 什么是元组？
# 元组是一个有序的、不可变的容器，可以存储多个数据
# 元组一旦创建，就不能修改

# 创建元组
fruits = ("苹果", "香蕉", "橙子")
print("水果元组:", fruits)

# 元组的特点：
# 1. 用小括号 () 创建
# 2. 有序（可以通过索引访问）
# 3. 不可变（创建后不能修改）
# 4. 可以存储不同类型的数据

# 创建单个元素的元组（注意逗号）
single = (1,)  # 必须加逗号
print("单个元素元组:", single)

# 创建空元组
empty_tuple = ()
print("空元组:", empty_tuple)

# 元组可以存储不同类型的数据
mixed = ("小明", 18, 1.75, True)
print("混合类型元组:", mixed)
