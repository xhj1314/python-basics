# 字典基础

# 什么是字典？
# 字典是一个存储"键-值"对的容器，就像一本真正的字典
# 每个词（键）对应一个解释（值）

# 创建字典
student = {
    "name": "小明",    # "name"是键，"小明"是值
    "age": 18,         # "age"是键，18是值
    "city": "北京"     # "city"是键，"北京"是值
}

# 打印字典
print("学生信息:", student)

# 字典的特点：
# 1. 用花括号 {} 创建
# 2. 每个元素由"键:值"组成
# 3. 键必须是唯一的
# 4. 键必须是不可变的（如字符串、数字、元组）
# 5. 值可以是任意类型

# 创建空字典
empty_dict = {}
print("空字典:", empty_dict)

# 创建不同类型的字典
prices = {
    "苹果": 5.5,
    "香蕉": 3.2,
    "橙子": 4.8
}
print("价格表:", prices)
