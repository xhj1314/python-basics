# 6️⃣ 基础练习题
# 创建一个列表 scores，包含 [85, 90, 78, 92]。
#
# 在末尾添加新成绩 88。
#
# 删除第二个成绩（索引1）。
#
# 检查是否存在成绩 95，若不存在打印“未找到”。
#
# 将列表反转并输出。



scores = [85, 90, 78, 92]
scores.append(88)        # → [85,90,78,92,88]
scores.pop(1)            # → 删除90 → [85,78,92,88]
if 95 not in scores:
    print("未找到")       # → 未找到
scores.reverse()
print(scores)            # → [88, 92, 78, 85]