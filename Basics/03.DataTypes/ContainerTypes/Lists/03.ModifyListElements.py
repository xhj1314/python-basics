# 修改列表元素

# 列表是可变的，可以修改列表中的元素

# 创建一个列表
fruits = ["苹果", "香蕉", "橙子", "葡萄"]
print("原始列表:", fruits)

# 修改单个元素
fruits[1] = "芒果"  # 将索引1的元素（香蕉）修改为芒果
print("修改后:", fruits)  # 输出: ['苹果', '芒果', '橙子', '葡萄']

# 修改多个元素
fruits[0:2] = ["西瓜", "梨"]  # 将索引0和1的元素替换为西瓜和梨
print("再次修改后:", fruits)  # 输出: ['西瓜', '梨', '橙子', '葡萄']

# 注意：修改元素时，索引必须在有效范围内
# fruits[10] = "桃子"  # 错误！索引10超出范围

# 示例：修改数字列表
numbers = [1, 2, 3, 4, 5]
print("\n原始数字列表:", numbers)

numbers[0] = 10  # 将第一个元素修改为10
print("修改后:", numbers)  # 输出: [10, 2, 3, 4, 5]

numbers[-1] = 50  # 将最后一个元素修改为50
print("再次修改后:", numbers)  # 输出: [10, 2, 3, 4, 50]
