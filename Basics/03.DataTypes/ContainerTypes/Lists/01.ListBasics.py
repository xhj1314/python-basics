# 列表基础定义

# 什么是列表？
# 列表是一个可以存储多个数据的容器，就像一个购物清单
# 列表中的每个数据叫做"元素"

# 创建一个简单的列表
fruits = ["苹果", "香蕉", "橙子"]
print("水果列表:", fruits)  # 输出: 水果列表: ['苹果', '香蕉', '橙子']

# 列表的特点：
# 1. 有序：元素按插入顺序存储
# 2. 可变：可以添加、删除、修改元素
# 3. 可重复：允许包含重复元素
# 4. 可以存储不同类型的数据

# 创建不同类型的列表
numbers = [1, 2, 3, 4, 5]  # 数字列表
print("数字列表:", numbers)

mixed = [10, "hello", True, [5, 6]]  # 混合类型列表
print("混合列表:", mixed)

empty = []  # 空列表
print("空列表:", empty)

# 列表的长度（元素个数）
print("水果列表的长度:", len(fruits))  # 输出: 3
print("数字列表的长度:", len(numbers))  # 输出: 5
