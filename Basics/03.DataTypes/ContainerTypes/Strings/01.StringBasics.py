# 字符串基础

# 什么是字符串？
# 字符串是用引号包围的文本，用于存储文字信息

# 创建字符串
name = "小明"           # 双引号
message = '你好世界'     # 单引号
long_text = """这是
多行
字符串"""              # 三引号

# 打印字符串
print("姓名:", name)
print("消息:", message)
print("长文本:", long_text)

# 字符串的特点：
# 1. 用引号包围（单引号、双引号、三引号）
# 2. 不可变（创建后不能修改）
# 3. 有序（可以通过索引访问）
# 4. 可以包含任何字符

# 转义字符
print("\n转义字符:")
print("换行: 第一行\n第二行")
print("制表符: 名字\t年龄")
print("引号: 他说\"你好\"")

# 原始字符串（不转义）
raw_string = r"C:\Users\name\folder"
print("\n原始字符串:", raw_string)
